           </div>
           <p align="left">We live in a world that seems to be filled with bad news. Did you know that the Bible is a book of good news? The great Christian preacher of the first century, the Apostle Paul, wrote these God-inspired words in the Bible, <em><strong>"I declare unto you the gospel which I preached unto you..." (1 Corinthians 15:1).</strong></em> The word "gospel" simply means "good news" or "a good message."</p>
           <br />
           <p align="left">So, "what is the gospel, the good news?" In that same chapter in the Bible, Paul explained the Good News.  He wrote, <em><strong>"For I delivered unto you first of all that which I also received, how that Christ died for our sins according to the scriptures; And that he was buried, and rose again the third day according to the scriptures" (1 Corinthians 15:3,4).</strong></em> That is the good news.</p>
           <br />
           <p align="left">In those few words we see three great truths that are taught all through the Bible:</p><br />
           <h3 align="left">1. You need a savior.</h3><br />
           <p align="left"><strong><em>"For all have sinned, and come short of the glory of God" (Romans 3:23). "For the wages of sin is death" (Romans 6:23)</em></strong>.</p>
           <br />
           <p align="left">The Bible teaches that the great evil of sin is  that it is against God Almighty. God is holy and just. He is perfect and  sinless. We are sinners for we have all broken God's standard of righteousness  - the Ten Commandments.</p>
           <br />
           <p align="left">There is no one who is good enough to earn God's favor or grace.  Sin has corrupted every member of the human family and we sin by nature and by choice.</p>
           <br />
           <p align="left">Death is the penalty for sin. That is what the Bible says. Jesus said, <strong><em><span class="style1">"Except ye repent, ye shall all likewise perish"</span> (Luke 13:5).</em></strong> When He spoke those words, He was speaking to people who had heard of an awful tragedy where eighteen lives were lost as a tower collapsed. He wanted His hearers to know that no one is exempt from the <em><strong>"wages of sin."</strong></em>  They needed to know that they needed to turn from their sins and trust the Lord Jesus to forgive them of their sins and save their souls.</p>
<br />
           <p align="left">Jesus, the Savior, loves people. That is why He warned that unrepentant sinners that die without a savior for their sins suffer in Hell. That is why He warned of Hell no less than three times in Mark 9:43-47.</p>
           <br />
           <p align="left">You see, you need a Savior from your sins and from the consequences of your sin.</p>
           <br />
           <h3 align="left">2. Christ died for your sins as your Savior.</h3>
           <br />
           <p align="left">Remember, <strong><em>"Christ died for our sins..."</em></strong> </p>
           <br />
           <p align="left">That is part of the good news. Someone already freely accepted the punishment and penalty for your sins. His name is Jesus Christ. You don't have to die and suffer in Hell for your sins. Jesus suffered for you when He died on the cross.</p>
           <br />
           <p align="left"><strong><em>"But God commendeth his love toward us, in that, while we were yet sinners, Christ died for us" (Romans 5:8).</em></strong></p>
           <br />
           <p align="left">That word <em><strong>"commendeth"</strong></em> simply means that God demonstrated or exhibited His love for sinners by sending His Son, Jesus, to die in our place. That is undeniable love!</p>
           <br />
           <p align="left">Jesus did not die on the cross because of anything that He had done wrong. The Bible teaches that as the Son of God He was absolutely perfect and sinless. But, He <em><strong>"bare our sins in his own body on the tree" (1 Peter 2:24)</strong></em> so that we could know forgiveness and pardon.</p>
           <br />
           <p align="left">Jesus died for you!</p>
           <br />
           <h3 align="left">3. Jesus rose again for you.</h3>
           <br />
           <p align="left"><em><strong>"And that He was buried and rose again..."</strong></em></p>
           <br /> 
           <p align="left">Have you ever wondered why Jesus rose from the dead? He rose again for you! When He came back from the dead, that event offered absolute proof that He was (and still is) the Son of God.</p>
           <br />
           <p align="left">Over five hundred eyewitnesses testified to the fact that Jesus died and rose again <strong>(1 Corinthians 15:6)</strong>.  That is why the Bible says that He offered <em><strong>"many infallible proofs"</strong></em> of His resurrection <em><strong>(Acts 1:3)</strong></em>. He wanted to make sure that you knew that He rose again so that you would trust Him alone as the only Savior from your sins and from Hell. You see, since He died for your sins, His return to life guarantees that you can absolutely trust Him to give you complete forgiveness.</p>
           <br />
           <p align="left">Maybe you are wondering: "How do I receive this salvation that Jesus obtained for me?"</p>
           <br /> 
           <p align="left">The Bible simply says that you must repent and  believe the gospel. Remember that Jesus said, <em><strong><span class="style1">"Except ye repent, ye shall all likewise perish..."</span></strong></em>To repent means to turn from your sinfulness,  your selfishness, your pride, your foolish ideas of being good enough, your  following of man-made religion . . . . You must be humble before Almighty God  and admit that you are a wicked sinner who deserves Hell. Admit to God that you  can do nothing to save yourself. Admit that your religion, your good deeds, your  own philosophy, and your self-righteousness cannot save you.</p>
           <br />
           <p align="left">Then, you must <em><strong>"Believe on the Lord Jesus Christ..." (Acts 16:31).</strong></em> Accept the good news that He died in your place and suffered for your sins. Accept the reality that He rose again from the dead. Trust the Lord Jesus Christ as your only Savior.</p>
           <br /> 
           <p align="left">Receive Him as your Lord and Savior by calling upon Him to save you. <em><strong>"For whosoever shall call upon the name of the Lord shall be saved" (Romans 10:13).</strong></em> He is the Lord.  You need to understand this and submit to Him as Lord.  Don’t fight Him.  Stop resisting Him.  Surrender to Him, trusting Him with all of your heart to save you from your sins.  Only Jesus can save you.  </p>
           <br /> 
           <p align="left">If you are interested in this message of the Gospel and want to learn more about it, please contact us so that we can help you from the Bible.</p>
