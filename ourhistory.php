           <div align="left"><img class="img_indent" alt="" src="images/historyphoto.png" /></div>
           <p align="left"><strong>Over the years, as the church has endeavored to honor and to obey the Lord Jesus Christ,</strong> He has added to His assembly according to His wisdom and purpose.  Just as the Scriptures promise, each member of Mid-Coast Baptist Church is gifted to serve the Lord and His church.</p>
           <br />
           <p align="left">The goal of Mid-Coast Baptist Church is simple: Glorify  God. The Bible reveals that God is glorified by praise and service to Him that is offered according to the  commands given in Scripture and from willing hearts made willing and able by His grace.</p>
