           <h3>A Biblical Baptist Church in Brunswick, Maine</h3>
           <br />
           <p align="left"><strong>Mid-Coast Baptist Church  was founded in 1999</strong> through the service of church planter, Bob Mitchell, Sr.,  and his son, Pastor Bobby Mitchell. They were sent to Brunswick by a New  Testament Baptist church in Virginia.&nbsp; As  co-founders of the church, they have worked together in the role of co-pastors,  with Pastor Bobby serving as lead pastor since 2002.</p>
