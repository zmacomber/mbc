           <p>Some of the ways in which the Lord is praised and served by Mid-Coast Baptist Church are:</p>
           <p><strong>Scriptural Singing of Psalms, Hymns, and Spiritual Songs<br />
Church Orchestra<br />
Tithing and Sacrificial Giving to Missions<br />
House-to-House Evangelizing<br />
Expositional Teaching and Preaching in the Assembly<br />
Keeping the Ordinances of Baptism and Communion<br />
Regular Prayer Meetings<br />
Holy Living in the Home and Society</strong></p><br />
