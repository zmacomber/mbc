           <h3 align="left">What should I wear?</h3>
<p align=left>We have found that most folks tend to dress up when they go to an event that they consider to be special. Our pastors and teachers, as well as those who are members of the church, wear the traditional "Sunday best," because we are convinced that there is nothing more special or more important than gathering together to worship the Lord.  If you don’t feel like you have “nice enough” clothes to attend and hear the Word of God preached, please don’t let that keep you away.  Your first concern needs to be whether or not you have been saved by the grace of God.</p>
<br />
<h3 align=left>Where do my children go?</h3>
<p align=left>Our children join us for church-wide prayer time in the auditorium at 10:00 Sunday morning.  They are then dismissed to their own Sunday School class.  Just like the adults, they are taught through books of the Bible by competent teachers who serve under the direction of the pastor.  During the morning worship service preaching time, children ages 4-8 participate in a special singing, teaching, and preaching time in the Fellowship Hall.  All ages are present for all aspects of the Sunday and Thursday evening services.</p>
<br />
<p align=left>During every service there is a safe, clean, well-staffed nursery for children three and younger.</p>
<br />  
<h3 align="left">Am I expected to put money in the offering?</h3>
<p align=left>If you are visiting with us, we don't expect you to put money in the offering plate, and you are welcome to just let it pass you by.</p>
<br />
<h3 align="left">What Bible Version do you use?</h3>
<p align=left>Based on the teaching of the Bible and on the testimony of history, we are persuaded that the King James Version is the English translation of the inspired and preserved Words of God that still exist in the Hebrew Masoretic Text of the Old Testament and the Textus Receptus of the New Testament.  If you do not have a copy of God’s Word in English (KJV), we will be happy to let you use one of our many “pew” Bibles here at the church building.</p>
<br />  
<h3 align="left">Will I have to stand up and tell my name or say anything publicly?</h3>
<p align=left>You will not be called out to introduce yourself, and you will not be pointed out by anyone from the platform.</p
><br />
<h3 align="left">Other questions?</h3>
<p align=left>If you have other questions about the doctrine and practice of Mid-Coast Baptist Church, you are invited to call the church office at 207-373-0818 or email Pastor Bobby Mitchell at <a href="mailto:pastor@midcoastbaptistchurch.com">pastor@midcoastbaptistchurch.com</a>.</p>
