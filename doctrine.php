           <p align="left">We believe that every Bible doctrine is important, and we take seriously the command of Jesus to teach His disciples “all things” that He commanded (Matthew 28:20).  Because of this, we want to be very clear that the following is not meant to be an exhaustive statement of faith but simply a brief summary of some of the Bible doctrines that we hold to.</p><br />

<p align="left"><strong>We believe that the Bible is the divinely inspired Word of God.</strong> We believe that God not only inspired His words as they were originally given (2 Timothy 3:16; 2 Peter 1:19-21), but we also accept His promise that He has preserved them perfectly for every generation including ours (Psalms 12:6,7; Proverbs 30:5,6; Matthew 24:35).  Based on the teaching of the Bible and the testimony of history, we are persuaded that the King James Version is the English translation of the inspired and preserved Words of God that still exist in the Hebrew Masoretic Text of the Old Testament and the Textus Receptus of the New Testament.</p>
<br />

<p align="left"><strong>We believe in the Triune God: Father, Son, and Holy Spirit.</strong> We believe that They are equal in every divine perfection, and that They execute distinct but harmonious offices in the great work of redemption (Deuteronomy 6:4,5; I Corinthians 8:6; Ephesians 4:6; I Timothy 1:17; Matthew 28:19; John 15:16,17,26).</p>
<br />

<p align="left"><strong>We believe the Genesis account of creation.</strong> We accept it as literal, not allegorical or figurative. We accept that man was created directly in God's own image and after His own likeness, that man's creation was not a matter of evolution, or evolutionary changes of species, or development through interminable periods of time from lower to higher forms (Genesis 1:1; Exodus 20:11; Acts 4:24; Colossians 1:16,17; Hebrews 11:3; John 1:3; Revelation 10:6; Romans 1:20; Acts 17:23-26; Jeremiah 10:12; Nehemiah 9:6; Genesis 1:11; Genesis 1:24).</p>
<br />

<p align="left"><strong>We believe that man was created in the image and after the likeness of God but, by voluntary transgression, fell from his sinless and happy estate.</strong> We accept the Bible teaching that the consequence of the Fall is that all men are now sinners by nature, choice, and practice. Therefore, unbelieving sinners are under the just condemnation of God, without defense or excuse (Genesis 3:1-6,24; Romans 5:12,19; Romans 3:10-19; Ephesians 2:1,3; Galatians 3:22).</p>
<br />

<p align="left"><strong>We believe in salvation by grace alone through faith alone. We accept that the Lord Jesus Christ is the only Savior from sin and Hell.</strong> (John 14:6; Acts 4:12; I Timothy 2:5). We believe that He died for the sins of all mankind as the sinless, suffering, sacrificial Lamb of God (I John 2:2; I Timothy 2:4; I Peter 2:21-24). We reject any other "gospel" that includes works or man's ideas.</p>
<br />

<p align="left"><strong>We believe in the literal death, burial, and resurrection of the Lord Jesus Christ.</strong> We accept that as the Gospel, and we preach it as the power of God to save souls (I Corinthians 15:1-4; Romans 1:15-17).</p>
<br />

<p align="left"><strong>We believe that Jesus is coming again.</strong> We accept the personal, imminent, pre-tribulation and pre-millennial coming of the Lord Jesus Christ for His redeemed ones, and His subsequent return to earth with His saints to establish His Millennial Kingdom. (I Thessalonians 4:13-18; Zechariah l4:4-11; Revelation 19:11-16; Revelation 20:1-6; Revelation 3:10).</p>
<br />

<p align="left"><strong>We believe that it is the responsibility of every born-again believer to be scripturally baptized into the body of a New Testament church.</strong> We believe that each and every New Testament Baptist church is the body of Christ, the temple of God, the house of God, the pillar and ground of the truth, and the organism through which God is reaching the world with the Gospel of Jesus Christ. We believe that it is the responsibility of every New Testament church to be working to fulfill the Great Commission, because the Great Commission (preaching the Gospel, baptizing converts, and establishing churches) was given to the church. (Matthew 28:18-20; Acts 2:38,41; I Corinthians 12:27; I Corinthians 3:16-17; I Timothy 3:15).</p>
